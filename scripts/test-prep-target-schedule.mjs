import { readFileSync } from 'node:fs'
import assert from 'node:assert/strict'

const apiSource = readFileSync(new URL('../src/api/question.ts', import.meta.url), 'utf8')
const viewSource = readFileSync(new URL('../src/views/MePrepView.vue', import.meta.url), 'utf8')

assert.match(apiSource, /interviewDate\?:\s*string/, 'PrepTarget must expose interviewDate')
assert.match(apiSource, /priority\?:.*string/, 'PrepTarget must expose priority')
assert.match(apiSource, /note\?:\s*string/, 'PrepTarget must expose note')
assert.match(apiSource, /interviewDate:\s*raw\?\.interviewDate \|\| undefined/, 'PrepTarget adapter must map interviewDate')
assert.match(apiSource, /priority:\s*raw\?\.priority \|\| undefined/, 'PrepTarget adapter must map priority')
assert.match(apiSource, /note:\s*raw\?\.note \|\| undefined/, 'PrepTarget adapter must map note')
assert.match(apiSource, /addPrepTarget: async \(data: \{ targetType: string; targetValue: string; interviewDate\?: string; priority\?: string; note\?: string \}\)/, 'addPrepTarget must send schedule metadata')

assert.match(viewSource, /targetForm = reactive\(\{ targetType: 'company', targetValue: '', interviewDate: '', priority: 'medium', note: '' \}\)/, 'MePrep target form must carry date priority and note')
assert.match(viewSource, /type="date"/, 'MePrep target form must render a target date input')
assert.match(viewSource, /近期目标/, 'MePrep target form must expose urgent priority with community-learning copy')
assert.match(viewSource, /maxlength="300"/, 'MePrep target note input must be length-limited')
assert.match(viewSource, /targetScheduleText\(target\)/, 'MePrep target chips must show schedule text')
assert.match(viewSource, /targetPriorityText\(target\.priority\)/, 'MePrep target chips must show priority text')
assert.match(viewSource, /targetScheduleText\(summary\.target\)/, 'MePrep target summaries must show schedule text')
assert.match(viewSource, /actionText="去知识库看看"/, 'MePrep unavailable empty state must offer a concrete next action')
assert.match(viewSource, /isPrepStarterEmpty/, 'MePrep must detect the all-zero starter state')
assert.match(viewSource, /先把学习空间喂进第一批数据/, 'MePrep starter state must explain how to create value')
assert.match(viewSource, /href="#prep-targets"/, 'MePrep starter state must jump to the target form')
assert.match(viewSource, /添加学习目标/, 'MePrep target form submit should explain the action instead of saying only add')
assert.match(viewSource, /先添加目标方向、岗位或技术标签/, 'MePrep empty target copy must mention all supported target types')
assert.match(viewSource, /还有 \$\{diffDays\} 天/, 'MePrep must show days until the target date')
assert.match(viewSource, /今天到期/, 'MePrep must highlight same-day targets')
assert.match(viewSource, /已过 \$\{Math\.abs\(diffDays\)\} 天/, 'MePrep must show overdue target dates')
assert.match(viewSource, /<RiskConfirmDialog[\s\S]*:state="riskConfirmState"/, 'MePrep target deletion must render the shared risk confirmation dialog')
assert.match(viewSource, /useRiskConfirm/, 'MePrep target deletion must use structured confirmation instead of direct destructive clicks')
assert.match(viewSource, /正在加载学习数据/, 'MePrep first-screen loading must clearly explain that learning data is loading')
assert.match(viewSource, /\.target-chip-remove[\s\S]*height: 2\.5rem;[\s\S]*width: 2\.5rem;/, 'MePrep target remove button must keep a 40px touch target')

const deleteTargetStart = viewSource.indexOf('const deleteTarget = async')
assert.notEqual(deleteTargetStart, -1, 'deleteTarget must exist')
const deleteTargetEnd = viewSource.indexOf('\nconst ', deleteTargetStart + 1)
const deleteTargetBody = viewSource.slice(deleteTargetStart, deleteTargetEnd === -1 ? viewSource.length : deleteTargetEnd)
const confirmIndex = deleteTargetBody.indexOf('confirmRisk')
const deleteApiIndex = deleteTargetBody.indexOf('questionApi.deletePrepTarget(id)')
assert.ok(confirmIndex >= 0, 'deleteTarget must request confirmation')
assert.ok(deleteApiIndex >= 0, 'deleteTarget must still call deletePrepTarget after confirmation')
assert.ok(confirmIndex < deleteApiIndex, 'deleteTarget must confirm before calling deletePrepTarget')

console.log('prep target schedule guard passed')
